function MissIon1203(UserWorldId,QuestId, StepQuest, StatusQuest)
  local Player = GetPlayer(UserWorldId)
--Cac buoc xu li nhiem vu
  if StepQuest==1 then
      if Player.Player_FLD_LEVEL<5 then 
          SendSysMsg(UserWorldId,"Ban phai dat cap do 5 moi co the nhan nhiem vu !", 9, "He thong")
